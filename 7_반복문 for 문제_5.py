# for 버전

h = 3 # 전체 높이 및 넓이

for star in range(1, h + 1): #  높이만큼 반복
  # 한줄에 별의 개수만큼 별을 그리는 반복문
  for i in range(star) : # 해당 라인에 그려야 할 별의 개수만큼 반복
    print("*", end="") # 한줄에 그려야 하기 때문에 줄바꿈 X
  print() # 한줄이 완성되면 다음줄로 줄바꿈



