# 딕셔너리와 리스트

## 딕셔너리 CRUD

# =================================================================================
# 선언
# =================================================================================
# 리스트 
list1 = []

# 딕셔너리
dict1 = {}

# =================================================================================
# 추가
# =================================================================================
# 리스트
list1 = list1 + [1]
list1.append(2)
print(list1)


# 딕셔너리 -> 키 : 밸류
# "이름" : "홍길동"
dict1["이름"] = "홍길동"
print(dict1)

# =================================================================================
# 조회
# =================================================================================
# 리스트
print(list1[0]) # 인덱스로 조회
# 딕셔너리
print(dict1["이름"]) # 키로 조회

# =================================================================================
# 수정
# =================================================================================
# 리스트 
list1[0] = 100
print(list1)

dict1["이름"] = "이순신" # 키가 존재하면 수정, 없으면 추가로 수행
print(dict1)

# =================================================================================
# 삭제
# =================================================================================
# 리스트
del(list1[0])
print(list1)

# 딕셔너리
del(dict1["이름"])
print(dict1)

# =================================================================================
# 기타 함수
# =================================================================================
# 전체 키를 반환
dict1["이름"] = "홍길동"
dict1["나이"] = 33
dict1["거주지"] = "대전"

for key in dict1.keys() :
    print(dict1[key])
